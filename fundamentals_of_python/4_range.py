# crate list using range function
zero_to_10 = list(range(11))
print(zero_to_10)

ten_to_20 = list(range(10, 21))
print(ten_to_20)

# odd numbers list using range
odd_numbers = list(range(0, 20, 2))
print("odd list -> ", odd_numbers)

# even numbers list using range
even_numbers = list(range(1, 20, 2))
print("even list -> ", even_numbers)

